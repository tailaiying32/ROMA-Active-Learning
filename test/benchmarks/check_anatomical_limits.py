
import sys
import os
import torch
import numpy as np
import yaml
from pathlib import Path

sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), '../../../')))

from active_learning.src.config import DEVICE, load_config, get_bounds_from_config
from active_learning.test.diagnostics.utils import load_diagnostic_model
from active_learning.src.latent_prior_generation import LatentPriorGenerator

def check_limits_compliance(decoder, embeddings, bounds_np, joint_names, dataset_name="Training Data"):
    """Check if embeddings decode to within bounds."""
    num_samples = embeddings.shape[0]
    batch_size = 100
    
    all_lower = []
    all_upper = []
    
    with torch.no_grad():
        for i in range(0, num_samples, batch_size):
            batch_emb = embeddings[i:min(i+batch_size, num_samples)].to(DEVICE)
            lower, upper, _, _, _ = decoder.decode_from_embedding(batch_emb)
            all_lower.append(lower.cpu().numpy())
            all_upper.append(upper.cpu().numpy())
            
    all_lower = np.concatenate(all_lower, axis=0)
    all_upper = np.concatenate(all_upper, axis=0)
    
    print(f"\n--- {dataset_name} Compliance ({num_samples} samples) ---")
    
    total_violations = 0
    any_violation_mask = np.zeros(num_samples, dtype=bool)
    
    for i, name in enumerate(joint_names):
        limit_lower = bounds_np[i, 0]
        limit_upper = bounds_np[i, 1]
        
        # Violations: Decoder range extends BEYOND limits
        # i.e. decoder_lower < limit_lower  OR  decoder_upper > limit_upper
        v_lower = all_lower[:, i] < limit_lower
        v_upper = all_upper[:, i] > limit_upper
        
        n_v_lower = np.sum(v_lower)
        n_v_upper = np.sum(v_upper)
        
        any_violation_mask |= v_lower
        any_violation_mask |= v_upper
        
        if n_v_lower > 0 or n_v_upper > 0:
            print(f"  {name}:")
            if n_v_lower > 0:
                print(f"    Lower violations: {n_v_lower}/{num_samples} ({n_v_lower/num_samples*100:.1f}%)")
            if n_v_upper > 0:
                print(f"    Upper violations: {n_v_upper}/{num_samples} ({n_v_upper/num_samples*100:.1f}%)")
                
    unique_violations = np.sum(any_violation_mask)
    print(f"  Total Unique Samples with Violations: {unique_violations}/{num_samples} ({unique_violations/num_samples*100:.1f}%)")
    return unique_violations, num_samples

def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("--model", type=str, default=None)
    parser.add_argument("--out-dir", type=str, default="active_learning/results/diagnostics")
    parser.add_argument("--n-prior-samples", type=int, default=1000)
    args = parser.parse_args()
    
    result_dir = Path(args.out_dir)
    result_dir.mkdir(parents=True, exist_ok=True)
    
    # Capture output to file
    class Logger(object):
        def __init__(self):
            self.terminal = sys.stdout
            self.log = open(result_dir / "limits.txt", "w")
        def write(self, message):
            self.terminal.write(message)
            self.log.write(message)
        def flush(self):
            self.terminal.flush()
            self.log.flush()
            
    sys.stdout = Logger()
    
    print(f"Running Anatomical Limits Diagnostic...")
    
    # 1. Load Model & Config
    # We need the full config for PriorGenerator, so we load active_learning default
    # and merge with model details if needed, but PriorGenerator mostly needs 'prior' section
    config = load_config()
    decoder, train_config, train_embeddings = load_diagnostic_model(args.model)
    
    # Extract bounds
    bounds = get_bounds_from_config(config, DEVICE)
    bounds_np = bounds.cpu().numpy()
    joint_names = config['prior']['joint_names']
    
    print("\nConfigured Limits:")
    for i, name in enumerate(joint_names):
         print(f"  {name}: [{np.rad2deg(bounds_np[i,0]):.1f}, {np.rad2deg(bounds_np[i,1]):.1f}] deg")

    # 2. Check Training Data
    check_limits_compliance(decoder, train_embeddings, bounds_np, joint_names, "Training Data")
    
    # 3. Check Healthy Prior Coverage
    print(f"\nGeneratng {args.n_prior_samples} samples from Healthy Prior...")
    prior_gen = LatentPriorGenerator(config, decoder, verbose=False)
    # Since get_prior returns a distribution, we just want raw samples
    # We can use the internal sampling logic of PriorGenerator or just get a prior mean?
    # Actually, PriorGenerator.get_prior() usually centers at mean. 
    # But we want to see the distribution of "generated priors" if we were to init for random users.
    # The current code generates a prior *distribution* for a single user.
    # To check "coverage of the latent space corresponding to healthy people", 
    # we should check the prior *mean* generated by the generator (which is seeded or projected).
    
    # If the prior is fixed (anatomical center), then we just check that one point?
    # Or does it have noise?
    # get_prior(None) -> generates a prior centered at anatomical limits (with some noise if configured).
    
    generated_z = []
    for _ in range(args.n_prior_samples):
        # We want independent samples of the prior mean
        # But get_prior might return the same thing if deterministic seed is used inside?
        # LatentPriorGenerator uses a generator if config has seed.
        # But we want to simulate "sampling different healthy users".
        # If 'mean_noise_std' is used, we get variety.
        dist = prior_gen.get_prior(ground_truth_z=None)
        generated_z.append(dist.mean)
        
    generated_z = torch.stack(generated_z)
    
    check_limits_compliance(decoder, generated_z, bounds_np, joint_names, "Generated Healthy Priors")
    
if __name__ == "__main__":
    main()
